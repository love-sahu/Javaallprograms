public class AreaPerimeterOfRec {

  public static void main(String[] args) {
    float h = 7.9f;
    float w = 4.5f;
    float area = w * h;
    float perimeter = 2 * (h + w);
    System.out.println("area of rectangle is " + area + "cm^2");
    System.out.println("perimeter of rectangle is " + perimeter + "cm");

  }
}